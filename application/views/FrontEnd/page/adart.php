            <p style="text-align: center;"><strong>ANGGARAN DASAR</strong><strong> DAN ANGGARAN RUMAH TANGGA</strong></p>
            <p style="text-align: center;"><strong>KELOMPOK KERJA PENGAWAS (POKJAWAS) PENDIDIKAN AGAMA ISLAM </strong></p>
            <p><strong>&nbsp;</strong></p>
            <p style="text-align: center;"><strong>PEMBUKAAN</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>Dengan rahmat Allah Swt Kelompok Kerja Pengawas (Pokjawas) Pendidikan Agama Islam Kementerian Agama RI sebagai salah satu komponen dalam dunia kependidikan memiliki posisi strategis disertai rasa tanggungjawab terhadap masa depan bangsa yang mendapatkan peran melalui pelaksanaan tugas pokok dan fungsinya sebagai pengawal/pembina terselenggaranya pendidikan Agama Islam&nbsp; yang bermutu dalam upaya mencapai tujuan pendidikan Nasional.</p>
            <p>&nbsp;Bahwa demi tercapainya cita cita tersebut, sumber daya manusia Pengawas Pendidikan Agama Islam harus ditingkatkan sehingga mampu melaksanakan tugasnya dengan baik</p>
            <p>&nbsp;Bahwa untuk dapat melaksanakan tugas pokok dan fungsinya , dipandang perlu untuk merapatkan barisan, mempersatukan derap langkah bergerak maju bersama sama membentuk satu wadah yang mampu mengantarkan pengawas Pendidikan Agama Islam menjadi pengawas yang memiliki 5 nilai budaya kerja. Dalam kaitan hal tersebut telah disepakati membentuk satu wadah&nbsp; sebagai&nbsp; tempat&nbsp; untuk&nbsp; berhimpun&nbsp; yaitu&nbsp; Kelompok&nbsp; Kerja&nbsp;&nbsp;&nbsp; Pengawas&nbsp; (Pokjawas) Pendidikan Agama Islam</p>
            <p>&nbsp;Bahwa demi tertib dan lancarnya gerak roda organisasi, maka disusun Anggaran Dasar dan Anggaran Rumah &nbsp;Tangga seperti tertuang dalam pasal pasal berikut :</p>
            <p style="text-align: center;">&nbsp;</p>
            <p style="text-align: center;"><strong>BAB I</strong></p>
            <p style="text-align: center;"><strong>NAMA, KEDUDUKAN, DAN WAKTU</strong></p>
            <p style="text-align: center;"><strong>&nbsp;</strong></p>
            <p style="text-align: center;"><strong>Pasal 1</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>Organisasi ini bernama &ldquo;Kelompok Kerja Pengawas Pendidikan Agama Islam&rdquo; &nbsp;Kementerian Agama RI disingkat dengan POKJAWAS-PAI Kementerian Agama RI</p>
            <p>&nbsp;</p>
            <p style="text-align: center;">Pasal 2</p>
            <p>&nbsp;</p>
            <p>( 1) POKJAWAS-PAI Pusat berkedudukan di Ibu Kota Negara Republik Indonesia</p>
            <p>( 2) POKJAWAS-PAI Provinsi berkedudukan di Ibu Kota Provinsi</p>
            <p>( 3) POKJAWAS-PAI Kabupaten / Kota berkedudukan di Ibu Kota Kabupaten / Kota</p>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>Pasal </strong><strong>3</strong></p>
            <p><strong>&nbsp;</strong></p>
            <ol>
                <li>POKJAWAS-PAI dibentuk pada Tanggal 23 September 2014 di Bogor.</li>
                <li>Selanjutnya tanggal sebagaimana disebut pada ayat (1) dinyatakan sebagai hari lahirnya Pokjawas PAI dan dijadikan hari peringatan POKJAWAS-PAI secara nasional setiap tahun</li>
            </ol>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>Pasal&nbsp; </strong><strong>4</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>Pokjawas-PAI adalah organisasi kedinasan yang bersifat kemitraan dan profesi yang didirikan oleh dan untuk Pengawas Pendidikan Agama Islam pada Sekolah Umum.</p>
            <p style="text-align: center;">&nbsp;</p>
            <p style="text-align: center;"><strong>BAB II</strong></p>
            <p style="text-align: center;"><strong>A</strong><strong>Z</strong><strong>AS,</strong><strong>VISI,MISI,</strong><strong>TUJUAN</strong><strong>,JATI DIRI,DAN SIFAT</strong></p>
            <p style="text-align: center;"><strong>&nbsp;</strong></p>
            <p style="text-align: center;"><strong>Pasal&nbsp; </strong><strong>5</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>Pokjawas-PAI adalah organisasi profesi pengawas Pendidikan Agama Islam yang berazaskan Pancasila dan Undang-undang Dasar 1945</p>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>Pasal </strong><strong>6</strong></p>
            <p><strong>&nbsp;</strong></p>
            <ol>
                <li>VISI :Terwujudnya Organisasi Pengawas Pendidikan Agama Islam yang Profesional, bermartabat dan Sejahtera</li>
                <li>MISI :</li>
            </ol>
            <ol style="list-style-type: lower-alpha;">
                <li>Meningkatkan kompetensi Pengawas Pendidikan Agama Islam yang Profesional</li>
                <li>Memfasilitasi profesi Pengawas Pendidikan Agama Islam yang sejahtera</li>
                <li>Membangun jatidiri Pengawas Pendidikan Agama Islam yang bermartabat</li>
                <li>Menjadi garda terdepan dalam penjaminan dan peningkatan mutu pendidikan Agama Islam disekolah umum</li>
            </ol>
            <p>&nbsp;</p>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>Pasal </strong><strong>7</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>POKJAWAS-PAI sebagai organisasi profesi Pengawas Pendidikan Agama Islam memiliki tujuan :</p>
            <ol style="list-style-type: lower-alpha;">
                <li>Meningkatkan kompetensi Pengawas Pendidikan Agama Islam yang Profesional</li>
                <li>Membangun jatidiri Pengawas Pendidikan Agama Islam yang bermartabat</li>
                <li>Memfasilitasi profesi Pengawas pendidikan Agama Islam yang sejahtera</li>
                <li>Menjadi garda terdepan dalam penjaminan dan peningkatan mutu pendidikan Agama Islam pada sekolah</li>
            </ol>
            <p style="text-align: center;">&nbsp;</p>
            <p style="text-align: center;"><strong>BAB III</strong></p>
            <p style="text-align: center;"><strong>FUNGSI DAN PERAN</strong></p>
            <p style="text-align: center;"><strong>&nbsp;</strong></p>
            <p style="text-align: center;"><strong>Pasal 8</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>POKJAWAS-PAI berfungsi sebagai :</p>
            <ol>
                <li>Wadah untuk menampung dan menyalurkan aspirasi pengawas Pendidikan Agama Islam</li>
                <li>Lembaga untuk menfasilitasi Pengawas Pendidikan Agama Islam dalam meningkatkan kompetensi dan profesionalisme</li>
                <li>Pemberi pertimbangan lisensi terhadap rekruitmen calon pengawas Pendidikan Agama Islam</li>
            </ol>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>Pasal 9</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>POKJAWAS-PAI berperan sebagai :</p>
            <ol>
                <li>Penyambung kebijakan pemerintah di bidang pendidikan kepada seluruh Pengawas Pendidikan Agama Islam</li>
                <li>Fasilitator bagi Pengawas Pendidikan Agama Islam dalam mengembangkan karir, kenaikan pangkat, dan memperoleh penghargaan serta apresiasi yang relevan</li>
                <li>Pelindung Pengawas Pendidikan Agama Islam dalam menjalankan tugas keprofesian</li>
            </ol>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>BAB IV</strong></p>
            <p style="text-align: center;"><strong>KEANGGOTAAN</strong></p>
            <p style="text-align: center;"><strong>&nbsp;</strong></p>
            <p style="text-align: center;"><strong>Pasal 10</strong></p>
            <p><strong>&nbsp;</strong></p>
            <ol>
                <li>Anggota Pokjawas-PAI adalah Pengawas Pendidikan Agama Islam di Negara Republik Indonesia maupun yang bertugas di luar negeri</li>
                <li>Anggota POKJAWAS-PAI terdiri atas :</li>
            </ol>
            <p>&nbsp;</p>
            <ol style="list-style-type: lower-alpha;">
                <li>Anggota Biasa</li>
                <li>Anggota Luar Biasa</li>
                <li>Anggota Kehormatan</li>
            </ol>
            <p>3. Anggota POKJAWAS-PAI wajib memiliki Kartu Anggota Pokjawas-PAI</p>
            <p>4. Ketentuan tentang kartu anggota POKJAWAS-PAI akan diatur dalam ketentuan Anggaran Rumah Tangga</p>
            <p><strong>&nbsp;</strong></p>
            <p style="text-align: center;"><strong>Pasal 11</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>Anggota Biasa :</p>
            <ol>
                <li>Anggota Biasa adalah pengawas Pendidikan Agama Islam pada Sekolah</li>
                <li>Keanggotaan Pokjawas-PAI sebagaimana disebutkan pada ayat (1) otomatis anggota aktif.</li>
            </ol>
            <p><strong>&nbsp;</strong></p>
            <p style="text-align: center;"><strong>Pasal 12</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>Anggota Luar Biasa : adalah Pengawas Pendidikan Agama Islam yang telah memasuki masa purna bhakti</p>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>Pasal 13</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>Anggota Kehormatan : adalah Pengawas Pendidikan Agama Islam yang telah memasuki masa purna bhakti dan anggota masyarakat yang peduli pendidikan khususnya pendidikan Agama Islam.</p>
            <p style="text-align: center;"><strong>&nbsp;</strong><strong>BAB IV</strong></p>
            <p style="text-align: center;"><strong>&nbsp;</strong><strong>HAK DAN KEWAJIBAN</strong><strong> ANGGOTA</strong></p>
            <p style="text-align: center;"><strong>&nbsp;</strong>&nbsp;<strong>Pasal 1</strong><strong>4</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>Anggota Biasa berhak :</p>
            <ol>
                <li>Menghadiri rapat,berbicara,dan memberikan suara</li>
                <li>Memilih dan dipilih sebagai pengurus Pokjawas-PAI</li>
                <li>Mendapatkan pelayanan professional,penghargaan,keselamatan kerja,dan perlindungan hukum dalam melaksanakan tugas kepengawasan</li>
                <li>Mendapatkan pelayanan dalam peningkatan kesejahteraan</li>
            </ol>
            <p>Anggota Biasa berkewajiban :</p>
            <ol>
                <li>Menjaga nama baik pengawas pendidikan Agama Islam sesuai dengan norma-norma Agama, budaya maupun peraturan perundang-undangan negara.</li>
                <li>Tunduk dan taat pada Anggaran Dasar dan Anggaran Rumah Tangga,Kode Etik Profesi, serta ketetapan dan keputusan Pokjawas-PAI</li>
                <li>Menjalankan tugas profesi dengan sebaik baiknya</li>
                <li>Bertanggung jawab terhadap hasil tugas profesinya</li>
            </ol>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>Pasal 15</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>Anggota Luar Biasa dan Anggota kehormatan Pokjawas PAI berhak menghadiri rapat dan berbicara</p>
            <p style="text-align: center;"><strong>Pasal 16</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>Anggota Luar Biasa dan Anggota kehormatan Pokjawas-PAI bekewajiban menjaga citra,martabat, dan nama baik Pokjawas-PAI</p>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>BAB VI</strong></p>
            <p style="text-align: center;"><strong>KODE ETIK DAN DEWAN KEHORMATAN</strong></p>
            <p style="text-align: center;"><strong>&nbsp;</strong></p>
            <p style="text-align: center;"><strong>Pasal 1</strong><strong>7</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p>Kode etik Profesi Pengawas Pendidikan Agama Islam adalah kode etik profesi yang tertuang dalam ketetapan Musyawarah Nasional Pokjawas-PAI</p>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>Pasal 1</strong><strong>8</strong></p>
            <p><strong>&nbsp;</strong></p>
            <ol>
                <li>Untuk mengawasi anggota Pokjawas agar tidak menyimpang dari kode etik dalam melaksanakan tugas profesinya dibentuk Dewan Kehormatan yang bersifat kemitraan</li>
                <li>Pembentukan Dewan Kehormatan sebagaimana ayat (1) diatur dalam Anggaran Rumah Tangga</li>
            </ol>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>Pasal 19</strong></p>
            <p>&nbsp;</p>
            <p>Dewan Kehormatan berfungsi membantu menangani masalah dan atau memberikan rekomendasi kepada pengurus Pokjawas bagi Anggota Pokjawas yang melanggar Kode Etik Profesi.</p>
            <p>&nbsp;</p>
            <p style="text-align: center;"><strong>BAB VII</strong></p>
            <p style="text-align: center;"><strong>KEPENGURUSAN DAN BIDANG TUGAS</strong></p>
            <p style="text-align: center;"><strong>&nbsp;</strong></p>
            <p style="text-align: center;"><strong>Pasal </strong><strong>20</strong></p>
            <p><strong>&nbsp;</strong></p>
            <ol>
                <li>Kepengurusan Pokjawas-PAI terdiri dari Pengurus Pokjawas-PAI Pusat, Pengurus Pokjawas PAI Provinsi, dan Pengurus Pokjawas PAI Kabupaten/Kota</li>
                <li>Susunan Pengurus Pokjawas-PAI Pusat, Pengurus Pokjawas PAI Provinsi, dan Pengurus Pokjawas-PAI Kabupaten/Kota disesuaikan dengan kebutuhan organisasi</li>
